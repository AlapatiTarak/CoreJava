package com.corejava.collections;

public class ListArray {
 
	  int c;
	  int d;
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
	public int getD() {
		return d;
	}
	public void setD(int d) {
		this.d = d;
	}
	
	@Override
	public String toString() {
		return "ListArray [c=" + c + ", d=" + d + "]";
	}
	 
 
 
}
